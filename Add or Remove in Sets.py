Alphabets = {"A", "B", "C"}
Alphabets.add("D")
print("Set after adding 'D':", Alphabets)

Alphabets.update(["E", "F"])
print("Set after adding 'E' and 'F':", Alphabets)

Alphabets.remove("B")
print("Set after removing 'B':", Alphabets)


Alphabets.discard("G")  
print("Set after discarding 'pear':", Alphabets)
